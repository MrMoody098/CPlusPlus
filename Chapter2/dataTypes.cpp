#include <cstdio>

#include <iostream>

int main() {
    int myInt = 10;
    float myFloat = 3.14;
    double myDouble = 2.71828;
    char myChar = 'A';
    bool myBool = true;

    std::cout << "Integer: " << myInt << std::endl;
    std::cout << "Float: " << myFloat << std::endl;
    std::cout << "Double: " << myDouble << std::endl;
    std::cout << "Character: " << myChar << std::endl;
    std::cout << "Boolean: " << myBool << std::endl;

    return 0;
}